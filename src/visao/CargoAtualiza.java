public void preencherCampos(Cargo c){
        campoCargo.setText(c.getNomeCargo());
        campoComissao.setText(c.getComissao());
        campoId.setText(c.getId());
                
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   
        Cargo c = new Cargo();
        c.setNomeCargo(campoCargo.getText());
        c.setComissao(campoComissao.getText());
        c.setId(campoId.getText());
        
        CargoDAO dao = new CargoDAO();
        status.setText(dao.atualizar(c));
        lista.atualizaTabela();
                
        campoCargo.setText("");
        campoComissao.setText("");
        campoId.setText("");
        
           
    }//GEN-LAST:event_jButton1ActionPerformed

